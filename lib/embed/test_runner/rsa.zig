// -----------------------------------------------------------------------
// 8. parseDer — garbage input must fail
// -----------------------------------------------------------------------

fn parseDerInvalidTest(comptime rsa: type) !void {
    const garbage = [_]u8{ 0xDE, 0xAD, 0xBE, 0xEF };
    if (rsa.parseDer(&garbage)) |_|
        return error.ShouldHaveFailed
    else |_| {}
}

