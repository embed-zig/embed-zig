//! RSA signature verification test runner.
//!
//! RSA is not part of std's public API, so it cannot be covered by
//! compact_test and is exercised in a dedicated runner.
//! Tests `embed.crypto.rsa` (parseDer, verifyPKCS1v1_5, verifyPSS)
//! using pre-computed 1024-bit RSA test vectors generated with OpenSSL.
//!
//! Usage:
//!   const rsa_runner = @import("embed").test_runner.rsa;
//!   try rsa_runner.run(embed);

pub fn run(comptime lib: type) !void {
    const rsa = lib.crypto.rsa;
    const HashType = rsa.HashType;

    try parseDerTest(rsa);
    try pkcs1v15VerifyTest(rsa, HashType);
    try pkcs1v15TamperedMsgTest(rsa, HashType);
    try pkcs1v15TamperedSigTest(rsa, HashType);
    try pssVerifyTest(rsa, HashType);
    try pssTamperedMsgTest(rsa, HashType);
    try pssTamperedSigTest(rsa, HashType);
    try parseDerInvalidTest(rsa);
    try wrongHashTest(rsa, HashType);
}

// -----------------------------------------------------------------------
// 1. parseDer — round-trip: parse raw RSA DER, check modulus/exponent
// -----------------------------------------------------------------------

fn parseDerTest(comptime rsa: type) !void {
    const dk = try rsa.parseDer(&test_pub_der);
    if (dk.modulus.len != 128) return error.ModulusLenMismatch;
    if (dk.exponent.len != 3) return error.ExponentLenMismatch;
    if (dk.exponent[0] != 0x01 or dk.exponent[1] != 0x00 or dk.exponent[2] != 0x01)
        return error.ExponentValueMismatch;
}

// -----------------------------------------------------------------------
// 2. PKCS#1 v1.5 — valid signature
// -----------------------------------------------------------------------

fn pkcs1v15VerifyTest(comptime rsa: type, comptime HashType: type) !void {
    try rsa.verifyPKCS1v1_5(&test_sig_pkcs1, &test_msg, &test_pub_der, HashType.sha256);
}

// -----------------------------------------------------------------------
// 3. PKCS#1 v1.5 — tampered message must fail
// -----------------------------------------------------------------------

fn pkcs1v15TamperedMsgTest(comptime rsa: type, comptime HashType: type) !void {
    var bad_msg = test_msg;
    bad_msg[0] ^= 0xff;
    if (rsa.verifyPKCS1v1_5(&test_sig_pkcs1, &bad_msg, &test_pub_der, HashType.sha256)) |_|
        return error.ShouldHaveFailed
    else |_| {}
}

// -----------------------------------------------------------------------
// 4. PKCS#1 v1.5 — tampered signature must fail
// -----------------------------------------------------------------------

fn pkcs1v15TamperedSigTest(comptime rsa: type, comptime HashType: type) !void {
